function colorDeconstructor(col){
    if (col === 'orange'){ return 'Orange is made by combining red and yellow!'}  
    if (col === 'purple'){return 'Purple is made by combining red and blue!'}
    if (col === 'green'){return 'Green is made by combining blue and yellow!'}

    return "Sorry, that's not a secondary color!"

    






}


module.exports = colorDeconstructor;